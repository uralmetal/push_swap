#include "libft.h"

char	*ft_strmap(const char *s, char (*f) (char))
{
	size_t			len;
	char			*ret;
	unsigned int	i;

	if (s == NULL || f == NULL)
		return (NULL);
	len = ft_strlen((char*)s);
	ret = (char*)malloc(len + 1);
	if (ret == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		ret[i] = f(s[i]);
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
